package com.mdh.datastructure.linknode;

/**
 *
 * @author madonghao
 * @date 2018/11/19
 */
public class Node {

    private int data;
    private Node next;

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }
}
